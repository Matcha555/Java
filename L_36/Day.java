package L_36;

public class Day {
    private int day;
    private int month;
    private int year;

    public Day(int d, int m, int y) {
        this.day = d;
        this.month = m;
        this.year = y;
    }

    public int getDay() {
        return this.day;
    }

    public void setDay(int d) {
        this.day = d;
    }

    public int getMonth() {
        return this.month;
    }

    public void setMonth(int m) {
        this.month = m;
    }

    public int getYear() {
        return this.year;
    }

    public void setYear(int y) {
        this.year = y;
    }
}
